# for loop with "in" keyword
#  for loop is used to iterate over a sequence (list, tuple, string) or other iterable objects.
#  Iterating over a sequence is called traversal.

#  Syntax:
#  for item in iterable_object:
#      # do something with item


#  item is a variable that will store the value of the next item in the sequence on every iteration of the for loop.
#  The body of for loop is separated from the rest of the code using indentation.
#  The body can contain one or more statements.
#  The body of for loop is executed as many times as there are items in the sequence.
#  The body of for loop is exited when the last item in the sequence has been encountered.

for item in "Zero to Mastery":
    print(item)

for item in [1, 2, 3, 4]:
    print(item)
